@dataclass(frozen=True, slots=True)
class FreshnessScenario:
    episode_id: str
    question: str
    gold_answer: str
    timestamp: int
    subject: str
    relation: str
    domain: str
    parametric_answer: str
    parametric_confidence: float
    recurrence_hint: float
    stability_hint: float
    volatility_hint: float
    contradiction_hint: float
    metadata: dict


def _doc(
    doc_id: str,
    text: str,
    answer: str,
    timestamp: int,
    trust: float,
    source: str = "freshness_snapshot",
) -> SupportDoc:
    return SupportDoc(
        doc_id=doc_id,
        text=text,
        answer=answer,
        source=source,
        timestamp=timestamp,
        trust=trust,
        relevance=0.0,
    )


