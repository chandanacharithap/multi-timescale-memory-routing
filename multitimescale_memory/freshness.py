def build_freshness_benchmark(limit: int | None = None) -> tuple[list[EpisodeInput], list[SupportDoc], dict[str, str]]:
    scenarios = _episodes()
    if limit is not None and limit > 0:
        scenarios = scenarios[:limit]
    corpus_docs = _corpus()
    subject_doc_prefix = {
        "AcmeAI": "fresh:acme:",
        "NovaPhone": "fresh:nova:",
        "Terra Robotics": "fresh:terra:",
        "Redwood Summit": "fresh:redwood:",
        "Harbor AI Expo": "",
    }
    episodes: list[EpisodeInput] = []
    for scenario in scenarios:
        metadata = {
            "benchmark_source": FRESHNESS_BENCHMARK_SOURCE,
            **scenario.metadata,
        }
        prefix = subject_doc_prefix.get(scenario.subject, "")
        support_docs = [
            doc
            for doc in corpus_docs
            if prefix and doc.doc_id.startswith(prefix) and doc.timestamp <= scenario.timestamp
        ]
        episodes.append(
            EpisodeInput(
                episode_id=scenario.episode_id,
                question=scenario.question,
                gold_answer=scenario.gold_answer,
                dataset_id="freshness",
                timestamp=scenario.timestamp,
                subject=scenario.subject,
                relation=scenario.relation,
                domain=scenario.domain,
                support_docs=support_docs,
                parametric_answer=scenario.parametric_answer,
                parametric_confidence=scenario.parametric_confidence,
                popularity_bin=0.3,
                recurrence_hint=scenario.recurrence_hint,
                stability_hint=scenario.stability_hint,
                volatility_hint=scenario.volatility_hint,
                contradiction_hint=scenario.contradiction_hint,
                freshness=True,
                update_expected=scenario.metadata.get("update_type") != "forgetting_probe",
                metadata=metadata,
            )
        )
    manifest = {
        "benchmark_source": FRESHNESS_BENCHMARK_SOURCE,
        "episodes": str(len(episodes)),
        "corpus_docs": str(len(corpus_docs)),
    }
    return episodes, corpus_docs, manifest
